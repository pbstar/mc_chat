<script>
	export default {
		data() {
			return {
				id: '',
				txurl: '',
				name: '',
			}
		},
		onLoad() {
			uni.getStorage({
				key: 'mc_chat_id',
				success: (res) => {
					this.getmine(res.data)
					this.id = res.data
				}
			})
		},
		methods: {
			getmine(id) {
				uni.request({
					url: 'http://api.mcweb.top/mcchat/getmine.php',
					method: 'GET',
					data: {
						id: id
					},
					success: (res) => {
						if (res.data.state == 101) {
							this.txurl = res.data.body.imgurl
							this.name = res.data.body.name
						}
					}
				});
			},
			toblog() {
				this.$refs["toast"].open({
					message: "开发中",
				});
			},
			toinfo(){
				uni.redirectTo({
					url: '/pages/mine/mineinfo?id='+this.id
				});
			}
		}
	}
